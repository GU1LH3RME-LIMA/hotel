    public double calcular(){
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd"); 
Calendar data1 = Calendar.getInstance();
Calendar data2 = Calendar.getInstance();
int valor1 = 0;
int valor2=0;
int valor3=0;
try {
data1.setTime(sdf.parse(checkin.getText()));
data2.setTime(sdf.parse(checkout.getText())); 
} catch (java.text.ParseException e ) {}
int dias = data2.get(Calendar.DAY_OF_YEAR) - 
data1.get(Calendar.DAY_OF_YEAR);
if (ComboTipo.getSelectedItem().equals("Standard")){
    valor1= dias*200;
}
if (ComboTipo.getSelectedItem().equals("Intermediario")){
     valor1= dias*350;
}
if (ComboTipo.getSelectedItem().equals("Luxo")){
     valor1= dias*500;
}
if (frigobar.getSelectedItem().equals("Sim")){
    valor2= dias*25;
   
}

if (bar.getSelectedItem().equals("Sim")){
    valor3= 200*Integer.parseInt((String) jComboBox1.getSelectedItem());
}
    double total=valor1+valor2+valor3;
       return total;
    }
    public void criarReserva(){
    Reserva pVO = new Reserva();
    calcular();
            pVO.setCpf(tCpf.getText());
            pVO.setCheckin(checkin.getText());
            pVO.setCheckout(checkout.getText());
            pVO.setTipo((String) ComboTipo.getSelectedItem());
            pVO.setNum(Integer.parseInt((String) ComboPessoas.getSelectedItem()));
            pVO.setFrigobar((String) frigobar.getSelectedItem());
            pVO.setBar((String) bar.getSelectedItem());
            pVO.setValor(calcular());
            pVO.setBarnum(Integer.parseInt((String) jComboBox1.getSelectedItem()));
            
            //Enviando o objeto pVO para o banco de dados
            ReservarDao dao = new ReservarDao();
dao.Reservar(pVO);

}
